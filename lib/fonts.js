export const DECORATIONS = {
  none: { name: "なし" },
  box: {
    name: "Box",
    render(lines) {
      const max = Math.max(...lines.map((l) => l.length));
      const top = "╭" + "─".repeat(max + 2) + "╮";
      const bot = "╰" + "─".repeat(max + 2) + "╯";
      const body = lines.map((l) => "│ " + l.padEnd(max) + " │");
      return [top, ...body, bot];
    },
  },
  double: {
    name: "Double",
    render(lines) {
      const max = Math.max(...lines.map((l) => l.length));
      const top = "╔" + "═".repeat(max + 2) + "╗";
      const bot = "╚" + "═".repeat(max + 2) + "╝";
      const body = lines.map((l) => "║ " + l.padEnd(max) + " ║");
      return [top, ...body, bot];
    },
  },
  stars: {
    name: "Stars",
    render(lines) {
      const max = Math.max(...lines.map((l) => l.length));
      const border = "✦" + "✧".repeat(max + 2) + "✦";
      const body = lines.map((l) => "✧ " + l.padEnd(max) + " ✧");
      return [border, ...body, border];
    },
  },
  line: {
    name: "Underline",
    render(lines) {
      const max = Math.max(...lines.map((l) => l.length));
      return [...lines, "━".repeat(max)];
    },
  },
};
