function makeCode(lines, fmt) {
  const art = lines.join("\n");
  if (fmt === "javascript") return `const banner = \`\n${art}\n\`;\n\nexport function printBanner() {\n  console.log("\\x1b[36m" + banner + "\\x1b[0m");\n}`;
  if (fmt === "python") return `BANNER = """\n${art}\n"""\n\ndef print_banner():\n    print(f"\\033[36m{BANNER}\\033[0m")`;
  if (fmt === "rust") return `pub fn print_banner() {\n    let banner = r#"\n${art}\n"#;\n    println!("\\x1b[36m{}\\x1b[0m", banner);\n}`;
  if (fmt === "go") return `package main\n\nimport "fmt"\n\nfunc PrintBanner() {\n\tbanner := \`\n${art}\n\`\n\tfmt.Printf("\\033[36m%s\\033[0m\\n", banner)\n}`;
  if (fmt === "shell") return `#!/bin/bash\nprint_banner() {\n  cat << 'BANNER'\n${art}\nBANNER\n}`;
  return art;
}

