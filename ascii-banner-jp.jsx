function render(text, styleKey, scale) {
  const { on, off } = STYLES[styleKey];
  const glyphs = [...text].map(ch => F[ch] || F[ch.toUpperCase()]).filter(Boolean);
  if (!glyphs.length) return [];
  const lines = [];
  for (let row = 0; row < 8; row++) {
    let line = "";
    for (const g of glyphs) {
      for (let bit = 6; bit >= 0; bit--) line += (g[row] >> bit) & 1 ? on : off;
      line += off;
    }
    for (let s = 0; s < scale; s++) lines.push(line);
  }
  return lines;
}

